#pragma once

// =============================================================================
// Таблица параметров Денавита-Хартенберга (DH) для 5-осевого манипулятора
//
// Классическое соглашение DH:
//   theta  - угол поворота вокруг оси Z_i-1 (для вращательных звеньев
//             это переменная, theta_offset добавляется к переменной из серво)
//   d      - смещение вдоль оси Z_i-1 (мм)
//   a      - длина звена вдоль оси X_i (мм)
//   alpha  - угол скручивания (twist) вокруг оси X_i (рад)
//
// Заполните значения для вашего манипулятора.
// =============================================================================

#include <cstddef>

struct DHParams {
    float theta_offset; // Смещение угла (рад): приводит угол серво к СК звена
    float d;            // (мм)
    float a;            // Длина звена (мм)
    float alpha;        // (рад)
};

// Количество звеньев (степеней свободы)
constexpr size_t DOF = 5;

// ┌──────────┬────────────────────┬──────────┬──────────┬──────────┐
// │  Звено   │   theta_offset     │    d     │    a     │  alpha   │
// ├──────────┼────────────────────┼──────────┼──────────┼──────────┤
// │    0     │                    │          │          │          │
// │    1     │                    │          │          │          │
// │    2     │                    │          │          │          │
// │    3     │                    │          │          │          │
// │    4     │                    │          │          │          │
// └──────────┴────────────────────┴──────────┴──────────┴──────────┘

