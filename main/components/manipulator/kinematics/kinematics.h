#pragma once

#include "dh_params.h"
#include <array>

// =============================================================================
// 4x4 матрица однородного преобразования
// =============================================================================
struct Matrix4x4 {
    float m[4][4];

    Matrix4x4();
    static Matrix4x4 identity();

    // Умножение матриц: *this * rhs
    Matrix4x4 operator*(const Matrix4x4& rhs) const;
};

// =============================================================================
// Вектор положения и ориентации конечного эффектора
// =============================================================================
struct Pose {
    float x, y, z;         // Положение (мм)
    float roll, pitch, yaw;// Ориентация (рад), извлекается из матрицы поворота
};

// =============================================================================
// Углы суставов (в радианах)
// =============================================================================
using JointAngles = std::array<float, DOF>;

// =============================================================================
// Класс кинематики манипулятора
// =============================================================================
class ManipulatorKinematics {
public:
    ManipulatorKinematics() = default;

    // -------------------------------------------------------------------------
    // Прямая задача кинематики (ПЗК)
    // Вход:  углы суставов (рад)
    // Выход: матрица однородного преобразования T_0_n (база -> TCP)
    //        и извлечённая из неё поза (x, y, z, roll, pitch, yaw)
    // -------------------------------------------------------------------------
    Matrix4x4 forwardKinematics(const JointAngles& q) const;

    // Удобная обёртка: возвращает только позу
    Pose computePose(const JointAngles& q) const;

    // Возвращает матрицу преобразования i-го звена относительно (i-1)-го
    // (используется внутри ПЗК и может пригодиться для отладки)
    Matrix4x4 linkTransform(size_t link_index, float theta) const;

    // -------------------------------------------------------------------------
    // Обратная задача кинематики (ОЗК)
    //
    // Вход:  target — целевая поза TCP (x, y, z — положение в мм;
    //                                   target.pitch — требуемый угол φ (рад);
    //                                   target.roll  — требуемый угол ψ (рад))
    // Выход: q_out  — вычисленные углы суставов (рад)
    //        Возвращает false, если точка недостижима (D > a₁+a₂ или cosθ₂ ∉ [-1,1])
    // -------------------------------------------------------------------------
    bool inverseKinematics(const Pose& target, JointAngles& q_out) const;

    // -------------------------------------------------------------------------
    // Интерполяция — планируется к реализации позднее
    // -------------------------------------------------------------------------
    // Блок будет содержать:
    //   - линейную/кубическую интерполяцию в пространстве суставов
    //   - линейную интерполяцию траектории TCP в декартовом пространстве
};

// =============================================================================
// Вспомогательные функции
// =============================================================================

// Перевод градусов в радианы
inline float degToRad(float deg) {
    return deg * 3.14159265358979323846f / 180.0f;
}

// Перевод радиан в градусы
inline float radToDeg(float rad) {
    return rad * 180.0f / 3.14159265358979323846f;
}
